<h1>Our History</h1>
<div>
Work Cat Trans Gulf (WCTG) is a short sea shipping operation that provides trans-Gulf of Mexico container and roll on/roll off freight transportation services between the ports of Tampa, Florida and Brownsville, Texas utilizing its own game-changing proprietary vessel design known as the “Work Cat.” 
<br />
<br /> 
The Company is the exclusive owner and operator of the fast and highly efficient Work Cat cargo vessel—a modern 400 foot catamaran capable of transporting 300 truckload equivalent shipments. Through its relationships with third party logistics providers, manufactured product shippers, and intermodal carriers, WCTG offers a completely unique alternative form of transportation capable of delivering fast, highly reliable service at low cost relative to all other land and sea-based transport modes. The economies of scale achieved by the Company’s unique catamaran cargo vessel enables the generation of a growing revenue stream and well above average return on investment.
